#!/usr/bin/env node
/**
 * scripts/revert-all-i18n.mjs
 *
 * Reverts every `prop: ({ t }) => t('website:<key>')` rewrite under
 * src/{collections,blocks,globals,fields}/**.{ts,tsx} back to a plain
 * string literal, sourced from `src/i18n/website.json` (en). Use this
 * when the key-derivation logic changes and you need to re-run the
 * extractor + refactor with clean keys.
 */

import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()
const SRC = path.join(ROOT, 'src')
const DIRS = ['collections', 'blocks', 'globals', 'fields']
const I18N_JSON = path.join(SRC, 'i18n/website.json')

const i18n = JSON.parse(readFileSync(I18N_JSON, 'utf8'))

const lookupEn = (key) => {
  // Keys generated by extract-codebase-labels.mjs use `:` as the path
  // separator (matching Payload's t() split convention).
  let cur = i18n.en
  for (const s of key.split(':')) {
    if (!cur || typeof cur !== 'object') return null
    cur = cur[s]
  }
  return typeof cur === 'string' ? cur : null
}

const escapeForSingleQuote = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")

const RE = /^(?<indent>\s*)(?<prop>label|description|singular|plural|group):\s*\(\{\s*t\s*\}\)\s*=>\s*t\('website:(?<key>[^']+)'\)\s*,?\s*$/

function walk(dir, out = []) {
  for (const entry of readdirSync(dir)) {
    const full = path.join(dir, entry)
    const stat = statSync(full)
    if (stat.isDirectory()) walk(full, out)
    else if (/\.(ts|tsx)$/.test(entry) && !entry.endsWith('.d.ts')) out.push(full)
  }
  return out
}

let touched = 0
let reverted = 0
let missing = 0
for (const d of DIRS) {
  let files
  try {
    files = walk(path.join(SRC, d))
  } catch {
    continue
  }
  for (const file of files) {
    const lines = readFileSync(file, 'utf8').split('\n')
    let mutated = false
    for (let i = 0; i < lines.length; i += 1) {
      const m = lines[i].match(RE)
      if (!m) continue
      const enValue = lookupEn(m.groups.key)
      if (enValue === null) {
        console.error(`! No English value for ${m.groups.key} in ${path.relative(ROOT, file)}:${i + 1}`)
        missing += 1
        continue
      }
      lines[i] = `${m.groups.indent}${m.groups.prop}: '${escapeForSingleQuote(enValue)}',`
      mutated = true
      reverted += 1
    }
    if (mutated) {
      writeFileSync(file, lines.join('\n'))
      touched += 1
    }
  }
}

console.log(`Reverted ${reverted} rewrite(s) across ${touched} file(s).`)
if (missing > 0) console.log(`Skipped ${missing} key(s) not found in website.json.`)
