#!/usr/bin/env node
/**
 * scripts/revert-placeholder-i18n.mjs
 *
 * Companion to `scripts/refactor-labels-to-i18n.mjs`. Payload's
 * `placeholder` prop is serialised straight to the client component and
 * does not accept a `LabelFunction`. Rewriting it to `({ t }) => t(...)`
 * triggers a Next.js client-component serialisation error at runtime.
 *
 * This script reverts ONLY `placeholder:` lines back to plain English
 * strings, sourced from `src/i18n/website.json` (en). Other rewrites
 * (label, description) stay as functions.
 */

import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()
const SRC = path.join(ROOT, 'src')
const DIRS = ['collections', 'blocks', 'globals', 'fields']
const I18N_JSON = path.join(SRC, 'i18n/website.json')

const i18n = JSON.parse(readFileSync(I18N_JSON, 'utf8'))

const lookupEn = (key) => {
  // Keys generated by extract-codebase-labels.mjs use `:` as the path
  // separator (matching Payload's t() split convention).
  const segs = key.split(':')
  let cur = i18n.en
  for (const s of segs) {
    if (!cur || typeof cur !== 'object') return null
    cur = cur[s]
  }
  return typeof cur === 'string' ? cur : null
}

const escapeForSingleQuote = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")

// `placeholder` and `group` are admin props serialised straight to the
// client component and do not accept LabelFunction. Revert both.
const RE = /^(?<indent>\s*)(?<prop>placeholder|group):\s*\(\{\s*t\s*\}\)\s*=>\s*t\('website:(?<key>[^']+)'\)\s*,?\s*$/

function walk(dir, out = []) {
  for (const entry of readdirSync(dir)) {
    const full = path.join(dir, entry)
    const stat = statSync(full)
    if (stat.isDirectory()) walk(full, out)
    else if (/\.(ts|tsx)$/.test(entry) && !entry.endsWith('.d.ts')) out.push(full)
  }
  return out
}

let touched = 0
let reverted = 0
for (const d of DIRS) {
  let files
  try {
    files = walk(path.join(SRC, d))
  } catch {
    continue
  }
  for (const file of files) {
    const lines = readFileSync(file, 'utf8').split('\n')
    let mutated = false
    for (let i = 0; i < lines.length; i += 1) {
      const m = lines[i].match(RE)
      if (!m) continue
      const enValue = lookupEn(m.groups.key)
      if (enValue === null) {
        console.error(`! No English value for key ${m.groups.key} in ${file}:${i + 1}`)
        continue
      }
      lines[i] = `${m.groups.indent}${m.groups.prop}: '${escapeForSingleQuote(enValue)}',`
      mutated = true
      reverted += 1
    }
    if (mutated) {
      writeFileSync(file, lines.join('\n'))
      touched += 1
    }
  }
}

console.log(`Reverted ${reverted} placeholder rewrite(s) across ${touched} file(s).`)
